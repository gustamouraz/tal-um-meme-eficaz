----CADASTRO DO ATIVO----
SELECT N1_CBASE, * FROM SN1010 WITH (NOLOCK)
    WHERE D_E_L_E_T_ = ''
    AND N1_DTCLASS <> ''
    AND N1_FILIAL+N1_CBASE IN (
        '0601030000003810',
        '0601010000003690',
        '060101000001422',
        '060101000001431',
        '060101000001432',
        '060101000001433',
        '060101000001423',
        '0601020000003693',
        '0601020000003689',
        '0601060000000006')
            ORDER BY R_E_C_N_O_

